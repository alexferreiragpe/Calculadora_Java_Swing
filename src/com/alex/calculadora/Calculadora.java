    public void Operacao() {
        valorFinal = Float.parseFloat(lblDigitacao.getText());
        lblVisor.setText(String.valueOf(valorInicial + " " + oper + " " + valorFinal));
        lblDigitacao.setText("");
        if (valorInicial != 0 && valorFinal != 0) {
            switch (oper) {
                case "+":
                    valorTotal = valorInicial + valorFinal;
                    break;
                case "-":
                    valorTotal = valorInicial - valorFinal;
                    break;
                case "*":
                    valorTotal = valorInicial * valorFinal;
                    break;
                case "÷":
                    valorTotal = valorInicial / valorFinal;
                    break;
                default:
                    break;
            }
            lblDigitacao.setText(String.valueOf(valorTotal));
            valorFinal = 0;
            valorInicial = 0;
        }

    }

    //apaga ultimo valor na tela
    public void LimpaUltimoValor() {
        PegaValor();
        if (temp.length() > 0) {
            temp = temp.substring(0, temp.length() - 1);
        }
    }

    //apaga campos
    public void LimpaCampos() {
        lblDigitacao.setText("0");
        lblVisor.setText("");
        temp = "";
        oper = "";
        valorFinal = 0;
        valorInicial = 0;
        valorTotal = 0;
    }

